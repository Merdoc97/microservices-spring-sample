package hello;

import javax.validation.constraints.NotNull;

public class HelloMessage {

    @NotNull
    private String name;

    public HelloMessage() {
    }

    public HelloMessage(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
